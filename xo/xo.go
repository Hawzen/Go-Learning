func check_win_condition(board *[3][3]byte) bool {
	// Check for horizontal win
	for _, row := range board {
		if row[0] == row[1] && row[1] == row[2] && row[0] != 0 {
			return true
		}
	}

	// Check for vertical win
	for i := 0; i < 3; i++ {
		if board[i][0] == board[i][1] && board[i][1] == board[i][2] && board[i][0] != 0 {
			return true
		}
	}

	// Check for diagonal win
	if board[0][0] == board[1][1] && board[1][1] == board[2][2] && board[1][1] != 0 {
		return true
	}
	if board[0][2] == board[1][1] && board[1][1] == board[2][0] && board[1][1] != 0 {
		return true
	}

	return false
}

func print_board(board *[3][3]byte) {
	for _, row := range board {
		for _, col := range row {
			// If the value is zero, print a space instead
			if col == 0 {
				col = ' '
			}
			fmt.Printf("%c  ", col)
		}
		fmt.Printf("\n\n")
	}
}

func insert_move(board *[3][3]byte, row int, col int, player byte) bool {
	if row > 2 || col > 2 || row < 0 || col < 0 {
		return false
	}

	if board[row][col] == 0 {
		board[row][col] = player
		return true
	}
	return false
}
